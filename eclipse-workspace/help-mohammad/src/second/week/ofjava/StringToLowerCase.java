package second.week.ofjava;

public class StringToLowerCase {

	
	public static void main(String[] args) {
		
		String city = "WOODBRIDGE";
		
		String cityAllLowercase = city.toLowerCase();
		
		System.out.println(cityAllLowercase);
		
		
		String name = "YUSUF";
		String nameInLowerCase = name.toLowerCase();
		
		System.out.println(nameInLowerCase);
		
		
	}
}

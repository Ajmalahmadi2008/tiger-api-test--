package forthweek.thirdday;

public class LoopNumbers {

   public static void main(String[] args) {
	
	   for(int start = 0; start <= 20; start ++) {
		   
		   if (start % 2 == 0) {
			   System.out.println("This is Even" + start);
			   
		   }else {
			   System.out.println("This is Odd" + start);
		   }
		   
	   }
}
}
